    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        //home menu example:
        if (jList1.getSelectedValue() != null) {
            String playerName = (String)jList1.getSelectedValue();
            Player[] pArray = selectedTeam.getPlayerArray();      //(player);
            Player p;
                    
            for(int i = 0; i<pArray.length; i++){
                if(pArray[i].getPlayerName().equals(playerName)){
                    p = pArray[i];
                    NBAPlayerProfileGUI n = new NBAPlayerProfileGUI(currentUser, currentLeague, selectedTeam, p);
                    n.setVisible(true);
                    this.dispose();
                }
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "Select a Player!");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

