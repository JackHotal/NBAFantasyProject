    public FantasyTeamPageGUI(User currentUser, League currentLeague, Team selectedTeam) {
        this();
        this.currentUser = currentUser;
        this.currentLeague = currentLeague;
        this.selectedTeam = selectedTeam;
        
        
        DefaultListModel m2 = (DefaultListModel)(jList1.getModel());
        m2.clear();
        Player[] players = selectedTeam.getPlayerArray();
        for(int i=0; i < players.length; i++) {
            if (players[i] != null) { 
                m2.addElement( players[i].getPlayerName() );
            }
        }
        
        jTextField3.setText(Integer.toString(selectedTeam.getWins()));
        
        jTextField2.setText(Integer.toString(selectedTeam.getLosses()));
        
        jTextField4.setText(Integer.toString(selectedTeam.getRank()));
        System.out.print("\n\n"+selectedTeam.getTeamName());
        jTextField1.setText(selectedTeam.getTeamName());
        System.out.print("\n"+currentLeague.getLeagueName());
        jTextField5.setText(currentLeague.getLeagueName());
        
        
    }

