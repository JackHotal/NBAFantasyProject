    public HomeMenuGUI(User currentUser){
        
        this();
        this.currentUser = currentUser;
        System.out.print("\nHome Menu current user: " + currentUser.getFirstName());
        LinkedList<League> systemsLeagues = NbaFantasyCenter.getAllLeagues();
        System.out.print("\nSystem's Leagues:");
        for(int i = 0; i<systemsLeagues.size(); i++){
            System.out.print("\n"+systemsLeagues.get(i).getLeagueName());
        }
        LinkedList<League> systemsHistLeagues = NbaFantasyCenter.getAllHistoricalLeagues();
        System.out.print("\nSystem's histLeagues:");
        for(int i = 0; i<systemsHistLeagues.size(); i++){
            System.out.print("\n"+systemsHistLeagues.get(i).getLeagueName());
        }
        
        
        //NbaFantasyCenter.setCurrentUser(currentUser);
        
        DefaultListModel m1 = (DefaultListModel)(jList3.getModel());
        //System.out.print("HOME MENU GUI - error is here");
        m1.clear();
        LinkedList<League> availableLeagues = NbaFantasyCenter.getAvailableLeagues();
        //DISPLAY LEAGUES
        //System.out.print(availableLeagues.get(1).getLeagueName());
        for(int i=0; i < availableLeagues.size(); i++) {
            m1.addElement( availableLeagues.get(i).getLeagueName() );
        }
        
        
        //display list of current User's leagues
        
        DefaultListModel m2 = (DefaultListModel)(jList1.getModel());
        m2.clear();
        League[] myLeagues = NbaFantasyCenter.getMyLeagues();
        for(int i=0; i < myLeagues.length; i++) {
            if (myLeagues[i] != null) { 
                m2.addElement( myLeagues[i].getLeagueName() );
            }
        }
        
        
        
        
        jTextField1.setText(currentUser.getFirstName());
    }

