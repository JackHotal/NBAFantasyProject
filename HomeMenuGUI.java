    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        
        //test available league list:
        //NbaFantasyCenter.addLeague("test 1", 10.50);
        //NbaFantasyCenter.addLeague("test 2", 10.60);
        
        
        if (jList1.getSelectedValue() != null) {
            String leagueName = (String)jList1.getSelectedValue();
            League l = NbaFantasyCenter.getCurrentUsersLeagueRefWithName(leagueName);
            LeaguePageGUI g = new LeaguePageGUI(currentUser, l);
            g.setVisible(true);
            this.dispose();
        }
        
        if ((jList3.getSelectedValue() != null)&&(currentUser.checkForMaxLeagues() == false)) {
            String leagueName = (String)jList3.getSelectedValue();
            League l = NbaFantasyCenter.getAvailableLeagueByName(leagueName);
            LeaguePageGUI g = new LeaguePageGUI(currentUser, l);
            g.setVisible(true);
            this.dispose();
        }
        
        
        
        
    }//GEN-LAST:event_jButton5ActionPerformed

    
